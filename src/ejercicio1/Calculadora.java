package ejercicio1;

public class Calculadora extends Dividir { 

	    public int sumar(int a, int b) { 
	        int resultado = a + b; 
	        return resultado; 
	    } 

	    public int restar(int a, int b) { 
	        int resultado = a - b; 
	        return resultado; 
	    } 

	    public int multiplicar(int a, int b) { 
	        int resultado = a * b; 
	        return resultado; 
	    } 

}
